/** ISO de N horas atrás (referência relativa à carga). */
function hoursAgo(h: number): string {
  return new Date(Date.now() - h * 3600_000).toISOString();
}

function daily(values: number[]): { date: string; mm: number }[] {
  return values.map((mm, i) => ({ date: `D+${i + 1}`, mm }));
}

