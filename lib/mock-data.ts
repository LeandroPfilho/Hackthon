/** Converte coordenadas [lon,lat] em GeoJSON LineString. */
export function toGeoJsonLine(coordinates: Segment["coordinates"]) {
  if (!coordinates || coordinates.length === 0) return null;
  return { type: "LineString" as const, coordinates };
}
